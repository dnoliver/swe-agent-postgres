SELECT 
  COALESCE(SUM(CASE WHEN m.TYPE = 'MedalBronze' THEN 1 ELSE 0 END), 0) as bronze_medals,
  COALESCE(SUM(CASE WHEN m.TYPE = 'MedalGold' THEN 1 ELSE 0 END), 0) as gold_medals,
  CASE 
    WHEN COALESCE(SUM(CASE WHEN m.TYPE = 'MedalBronze' THEN 1 ELSE 0 END), 0) > COALESCE(SUM(CASE WHEN m.TYPE = 'MedalGold' THEN 1 ELSE 0 END), 0) THEN 'Yes'
    ELSE 'No'
  END as answer
FROM ATHLETE a 
LEFT JOIN TOURNAMENT t ON a.ATHLETE_ID = t.ATHLETE_ID 
LEFT JOIN FORMAT f ON t.TOURNAMENT_ID = f.TOURNAMENT_ID 
LEFT JOIN MEDAL m ON f.FORMAT_ID = m.FORMAT_ID
WHERE a.NAME = 'Jessica von Bredow-Werndl';
